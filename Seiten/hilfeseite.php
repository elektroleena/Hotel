            <h3>Wie frage ich ein Zimmer an?</h3>
            <ol>
                <li>Registrieren Sie sich oder loggen Sie sich bei einem bereits bestehenden Konto ein.</li>
                <li>Klicken Sie auf "Reservierungen" in der Navigationsleiste.</li>
                <li>Wählen Sie "Neue Reservierung".</li>
                <li>Wählen Sie den Zeitraum Ihres Aufenthaltes aus.</li>
                <li>Geben Sie die gewünschte Anzahl der Zimmer an.</li>
                <li>Wählen Sie die gewünschten optionalen Zusatzleistungen (Frühstück, Parkplatz, Mitnahme von Haustieren).</li>
                <li>Klicken Sie auf "Anfragen".</li>
            </ol>


            <h3>Wie kann ich meine hinterlegten Stammdaten ändern?</h3>
            <ol>
                <li>Klicken Sie auf Ihren Benutzernamen rechts in der Navigationsleiste um Ihr Profil aufzurufen.</li>
                <li>Wählen Sie das Bleistift Symbol neben "Ihre Stammdaten".</li>
                <li>Führen Sie die gewünschten Änderungen durch und beachten Sie dabei, dass Ihr Passwort angegeben werden muss.</li>
                <li>Klicken Sie auf "Ändern".</li>
            </ol>

            <h3>Was, wenn ich mein Passwort vergessen habe oder es ändern möchte?</h3>
            <ul>
                <li>Sie haben Ihr Passwort vergessen:</li><br>
                <p>Wenden Sie sich in diesem Fall telefonisch an uns, wir helfen Ihnen gerne weiter.</p><br>
                <li>Sie möchten Ihr Passwort ändern:</li><br>
                <ol>
                    <li>Klicken Sie auf Ihren Benutzernamen rechts in der Navigationsleiste um Ihr Profil aufzurufen.</li>
                    <li>Scrollen Sie nach unten und klicken Sie auf "Passwort ändern"</li>
                    <li>Geben Sie im ersten Feld Ihr aktuelles Passwort ein.</li>
                    <li>Geben Sie im zweiten Feld Ihr neues Passwort ein und bestätigen Sie es im dritten Feld.</li>
                    <li>Klicken Sie auf "Ändern".</li>
